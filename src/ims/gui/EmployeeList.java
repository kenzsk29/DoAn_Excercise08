    public void refreshTable() {
        list = bll.readEmployeeList();
        int row = 0;
        model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        for (MyEmployee epl : list.listEmployee) {
            String ngaySinh = "" + epl.getNgaySinh() + " / " + epl.getThangSinh() + " / " + epl.getNamSinh() + "";
            model.addRow(new Object[0]);
            model.setValueAt(epl.getMaNV(), row, 0);
            model.setValueAt(epl.getHoDem(), row, 1);
            model.setValueAt(epl.getTen(), row, 2);
            model.setValueAt(ngaySinh, row, 3);
            row++;
        }
    }

