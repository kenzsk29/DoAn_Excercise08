    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed

        int row = jTable1.getSelectedRow();
        String id = jTable1.getModel().getValueAt(row, 0).toString();

        MyEmployee epl = findEmployee(Integer.parseInt(id));
        Team dcs = bll.readDCS(Integer.parseInt(id));
        Team dtn = bll.readDTN(Integer.parseInt(id));
        Team qd = bll.readQD(Integer.parseInt(id));
        if (epl == null) {
            JOptionPane.showMessageDialog(null, "Không tìm thấy");
        }

        Employee emGui = new Employee(epl, dcs, dtn, qd);
        emGui.setVisible(true);
    }//GEN-LAST:event_btnSuaActionPerformed

    public MyEmployee findEmployee(int id) {
        for (MyEmployee epl : list.listEmployee) {
            if (epl.getMaNV() == id) {
                return epl;
            }
        }
        return null;
    }
